     <h4>Kent Bryant<small></small></h4>
     <p>
Started in the fire service in 1984 in California.  I had joined Orange Co Fire Dept as a paid per call firefighter.  The dept put me through some mini academy training&#39;s.  My crew was a hand crew (Crew 50) which fought wild land fires.  We also helped the investigation teams on structure fires and did some clean up.  After about 3 years I moved closer to a first responding station (25) in Midway City.  We would respond to the station and would take the engine to all emergencies in the area.  We were backed up by a full time station out of Stanton, and Mutual aid with Westminster.  I attended Santa Ana College and received my Degree in Public Fire Service and attended  the 73rd basic fire academy which went on for about 3 months.  After graduation I secured a position with the Santa Ana Fire Dept as a reserve firefighter.  This dept also put me through a mini training academy.   This meant that I was the 4th person on a truck.  I had the opportunity to work from both engines and tiller style ladder trucks.  Both departments were very busy and I feel my experiences there are priceless.  Later we moved to Ohio near family where I started volunteering again.  I obtained my EMT certification, then Paramedic as well as 240 fire certification.  I worked with Miami Twp for about 9 years, Milford for 2 yrs, and Salem for 1.  In the mean time I also worked for Bethel-Tate part time for a year until I gained a full time position almost 10 years ago.  On my days off I help provide for my family as a State licensed plumbing contractor.  When I do have a break to relax,  motorcycle riding and fishing are my go to activities.
