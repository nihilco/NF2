     <h4>Richard A. Stowell<small>, Chief</small></h4>
     <p>
     Born April 22, 1961 to my parents, Richard and Lou and grew up in Bethel my entire life. Joined the fire department as a Fire Cadet August 1978 while a senior in high school and also worked at Lories Market as a stock boy and working at the meat counter. Learned how to cut meat while employed at Lories. Graduated from Bethel-Tate in 1979, became a regular volunteer fireman and got a part time meat cutting job at Kroger in Montgomery after my day job at Lories. Did that for 2 years, then was hired as a fulltime meat cutter at Bethel IGA, leaving Lories and Kroger. While working at IGA, John Munafo would let me leave work to make fire runs during the day, which back in the early 1980’s, we only had 2 or 3 firemen who was around during the day. I was promoted to Lt. in 1987, left Bethel IGA after being hired as a fulltime firefighter by Tate Township in March 1991, promoted to Captain in 1996, and appointed by the Tate Township Trustees as fulltime Fire Chief Jan 1, 2001. I am the oldest active firefighter on the department, feels like just a few years ago I was the youngest. I’ve been married to my wife Debbie (Crumpton) Stowell for 34 years, have one daughter, Kristen (Wilson) and a grandson Parker, who is more than likely going to carry on the family tradition of being a firefighter. I have less than 2 years until I’m eligible for retirement. Certifications through the State of Ohio are Firefighter II, Fire Inspector, Emergency Medical Technician-Basic, and Hazmat Tech. I’ve been on the department a total of 36 1/2 years, 24 years fulltime.
