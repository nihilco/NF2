    Deputy Erik Pike is assigned to Tate Township and operates out of  the Tate Township sub-station located at 146 North East Street, Bethel, Ohio 45106. Deputy Pike is supplemented by patrol and investigative service units operating out of the main Sheriff’s Office headquarters located at 4470 St. Rt. 222, Batavia, Ohio 45103.
</p>
<p>
    The Tate Township sub-station may be contacted at 734-7088 for NON-EMERGENCY use only.  There may be a delay in the return of your call.
</p>
